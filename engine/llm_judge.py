"""Multi-Judge evaluation engine.

Calls two independent OpenAI models as Judges on the same (question, answer, ground_truth)
triple, then computes agreement-rate and a lightweight Cohen's Kappa so we can detect
single-judge bias and flag high-disagreement cases for human review.
"""

from __future__ import annotations

import asyncio
import json
import os
import re
from typing import Any, Dict, List, Optional

from dotenv import load_dotenv
from openai import AsyncOpenAI

load_dotenv()


RUBRIC_PROMPT = """Bạn là một Judge chấm điểm câu trả lời của AI Agent.

Chấm điểm 3 tiêu chí, mỗi tiêu chí thang 1-5 (5 = xuất sắc, 1 = rất tệ):
1. accuracy — Mức chính xác so với ground_truth.
2. completeness — Câu trả lời có đầy đủ thông tin cần thiết không.
3. tone — Mức độ chuyên nghiệp, rõ ràng của ngôn từ.

CHỈ trả về JSON object hợp lệ, không có markdown, không có text khác:
{"accuracy": <1-5>, "completeness": <1-5>, "tone": <1-5>, "reasoning": "<ngắn gọn 1-2 câu>"}
"""


def _build_user_prompt(
    question: str, answer: str, ground_truth: str, answer_first: bool = False
) -> str:
    """`answer_first=True` swaps the order of the two blocks (used for position-bias test)."""
    if answer_first:
        return (
            f"CÂU HỎI: {question}\n\n"
            f"CÂU TRẢ LỜI CỦA AGENT: {answer}\n\n"
            f"GROUND TRUTH: {ground_truth}\n\n"
            "Hãy chấm điểm và trả về JSON."
        )
    return (
        f"CÂU HỎI: {question}\n\n"
        f"GROUND TRUTH: {ground_truth}\n\n"
        f"CÂU TRẢ LỜI CỦA AGENT: {answer}\n\n"
        "Hãy chấm điểm và trả về JSON."
    )


def _extract_json(text: str) -> Dict[str, Any]:
    """Best-effort parse — strips ```json fences and picks the first {...} block."""
    if "```json" in text:
        text = text.split("```json", 1)[1].split("```", 1)[0]
    elif "```" in text:
        text = text.split("```", 1)[1].split("```", 1)[0]
    match = re.search(r"\{[\s\S]*\}", text)
    if not match:
        raise ValueError(f"No JSON object in judge response: {text[:200]}")
    return json.loads(match.group(0))


class LLMJudge:
    """Two-model OpenAI consensus judge."""

    def __init__(
        self,
        model_a: Optional[str] = None,
        model_b: Optional[str] = None,
        client: Optional[AsyncOpenAI] = None,
    ):
        # Defaults avoid gpt-4o-mini on purpose: the golden set was generated by
        # gpt-4o-mini, so using it as a Judge would trigger self-preference bias.
        self.model_a = model_a or os.getenv("JUDGE_MODEL_A", "gpt-4o")
        self.model_b = model_b or os.getenv("JUDGE_MODEL_B", "gpt-4-turbo")
        if self.model_a == self.model_b:
            raise ValueError(
                f"Multi-Judge requires two DIFFERENT models; got {self.model_a} twice."
            )
        self._client = client or AsyncOpenAI()

    async def _score_one(
        self,
        model: str,
        question: str,
        answer: str,
        ground_truth: str,
        answer_first: bool = False,
    ) -> Dict[str, Any]:
        resp = await self._client.chat.completions.create(
            model=model,
            temperature=0.0,
            max_tokens=300,
            messages=[
                {"role": "system", "content": RUBRIC_PROMPT},
                {"role": "user", "content": _build_user_prompt(question, answer, ground_truth, answer_first)},
            ],
        )
        raw = resp.choices[0].message.content or ""
        parsed = _extract_json(raw)
        for k in ("accuracy", "completeness", "tone"):
            parsed[k] = max(1, min(5, int(parsed.get(k, 1))))
        parsed["overall"] = round((parsed["accuracy"] + parsed["completeness"] + parsed["tone"]) / 3, 2)
        parsed["_model"] = model
        parsed["_usage"] = {
            "prompt_tokens": resp.usage.prompt_tokens if resp.usage else 0,
            "completion_tokens": resp.usage.completion_tokens if resp.usage else 0,
        }
        return parsed

    async def evaluate_multi_judge(
        self, question: str, answer: str, ground_truth: str
    ) -> Dict[str, Any]:
        """Run both judges in parallel and aggregate."""
        score_a, score_b = await asyncio.gather(
            self._score_one(self.model_a, question, answer, ground_truth),
            self._score_one(self.model_b, question, answer, ground_truth),
        )

        overall_a, overall_b = score_a["overall"], score_b["overall"]
        gap = abs(overall_a - overall_b)
        # Agreement-rate: 1.0 if identical, linearly down to 0 at 4-point gap (max scale)
        agreement_rate = round(max(0.0, 1.0 - gap / 4.0), 3)
        final_score = round((overall_a + overall_b) / 2, 2)
        conflict = gap > 1.0  # flag for human review

        # Lightweight per-criterion Cohen-style agreement: fraction of criteria with equal integer score
        criteria = ("accuracy", "completeness", "tone")
        exact_matches = sum(1 for c in criteria if score_a[c] == score_b[c])
        kappa_like = round(exact_matches / len(criteria), 3)

        return {
            "final_score": final_score,
            "agreement_rate": agreement_rate,
            "kappa_like": kappa_like,
            "conflict": conflict,
            "gap": round(gap, 2),
            "individual_scores": {
                self.model_a: {k: score_a[k] for k in (*criteria, "overall", "reasoning")},
                self.model_b: {k: score_b[k] for k in (*criteria, "overall", "reasoning")},
            },
            "usage": {
                self.model_a: score_a["_usage"],
                self.model_b: score_b["_usage"],
            },
        }

    async def check_position_bias(
        self,
        question: str,
        answer: str,
        ground_truth: str,
        flip_threshold: float = 0.5,
    ) -> Dict[str, Any]:
        """Run Judge A twice — normal order and swapped order — and measure score drift.

        Returns {normal, swapped, delta, flipped} where `flipped` is True when the overall
        score changed by more than `flip_threshold`, i.e. the judge is sensitive to text
        position rather than its semantic label.
        """
        normal, swapped = await asyncio.gather(
            self._score_one(self.model_a, question, answer, ground_truth, answer_first=False),
            self._score_one(self.model_a, question, answer, ground_truth, answer_first=True),
        )
        delta = round(abs(normal["overall"] - swapped["overall"]), 2)
        return {
            "model": self.model_a,
            "normal_overall": normal["overall"],
            "swapped_overall": swapped["overall"],
            "delta": delta,
            "flipped": delta > flip_threshold,
            "usage": {
                "prompt_tokens": normal["_usage"]["prompt_tokens"] + swapped["_usage"]["prompt_tokens"],
                "completion_tokens": normal["_usage"]["completion_tokens"] + swapped["_usage"]["completion_tokens"],
            },
        }


def aggregate_usage(per_case_usages: List[Dict[str, Dict[str, int]]]) -> Dict[str, Dict[str, int]]:
    """Sum prompt/completion tokens across cases, keyed by model."""
    totals: Dict[str, Dict[str, int]] = {}
    for u in per_case_usages:
        for model, counts in u.items():
            bucket = totals.setdefault(model, {"prompt_tokens": 0, "completion_tokens": 0})
            bucket["prompt_tokens"] += counts.get("prompt_tokens", 0)
            bucket["completion_tokens"] += counts.get("completion_tokens", 0)
    return totals
